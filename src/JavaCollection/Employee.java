package JavaCollection;

public class Employee {
    public int getEmpId() {
        return empId;
    }

    public void setEmpId(int empId) {
        this.empId = empId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMoNo() {
        return moNo;
    }

    public void setMoNo(int moNo) {
        this.moNo = moNo;
    }

    int empId;
    String name;
    int moNo;


}
